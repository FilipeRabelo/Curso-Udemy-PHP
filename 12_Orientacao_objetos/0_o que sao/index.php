
        <h3>O que são objeots?</h3>
<p>
    Objetos sao entidades que possuem COMPORTAMENTOS e CARACTERISTICAS; <br>
    
    As CARACTERISTICAS ==> PROPRIEDADES ==> VARIAVEIS; <br>

    Os COMPORTAMENTOS  ==> METADOS ==> FUNÇÕES <br>

    Os objetos interagem entre si e sistemas sao escritos orientados 
    a objetos "POO" <br>
    PHP desenvolvemos neste paradigma <br>
</p>

        <h3>O que são classes?</h3>
<p>
    Classes são os PAIS dos Objetos; <br>

    Em PHP sempre que vamos criar ou INSTANCIAR um objeto 
    vamos precisar de uma classe <br>

    A classe contem o MOLDE Do OBJETO, ou seja, 
    seus METADOS e suas PROPRIEDADES; <br>
    
    Podemos mudar o valor para cada objeto criado, 
    mas ele parte do que a classe impoem <br>
</p>

