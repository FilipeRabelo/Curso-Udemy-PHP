<h3>Variaveis Globais do PHP</h3>

<p>
    <span>$_ENV: </span>Variaveis de ambiente
</p>

<p>
    <span>$_GET: </span>Parametros que foram enviados por request <span>GET</span>
</p>

<p>
    <span>$_POST: </span>Parametros que foram enviados por <span>POST</span>
</p>

<p>
    <span>$_COOKIE: </span>Valores de <span>COOKIES</span>
</p>

<p>
    <span>$_SERVER: </span>Informações sobre o SERVIDOR
</p>

<p>
    <span>$_FILES: </span>Informaçoes sobre os arquivos que vieram por upload
</p>
