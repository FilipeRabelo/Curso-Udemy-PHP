<h1>Banco de dados relacionais</h1>
<h2>
    
    Structured Query Language, ou Linguagem de Consulta Estruturada ou SQL, <br>
    é a linguagem de pesquisa declarativa padrão para banco de dados relacional. <br> 
    Muitas das características originais do SQL foram inspiradas na álgebra relacional. <br> <hr>

    O banco de dados relacionais tem como caracteristica principal trabalhar com tabelas; <br>
    O PHP é muito utilizado com DBs relacionais, como o MySQLi; <br>
    A linguagem para operação com esses bancos é a SQL <br>
    As intruções em SQL costumam ser escritas em letras maiusculas <br>

</h2>