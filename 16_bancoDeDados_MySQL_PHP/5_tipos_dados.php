PRINCIPAIS TIPOS DE DADOS:
Os tipos de dados funcionam como os tipos de variáveis, porem em varios “níveis” para melhor PERFORMECE;
•	VARCHAR: Texto de 0a 65.535 CARACTERES. Ex: Nome, e-mail, endereço ...
Obs.: podemos limitar o tamanho do VARCHAR 
•	TEXT: Texto como maximo de 65.535 BYTES. Ex: Texto de um blog...
•	INT: Números inteiros,
•	BIGINT: Números inteiros com maior proporção que o INT.
•	DATE: Data no formato YYYY-MM-DD;

